package day04_Variables;

public class PrimitivesIntro {
    public static void main(String[] args) {
        // age: 38 years old
        byte age = 38;
        // weight= 140 pounds;
        short weight = 160;
        //integer = perfect data type for whole numbers; preferred one
        int salary = 100_000;
        //add lower or uppercase L, if it's more than the value
        long asset = 3_333_33333333L;
        //double preferred data type for all the decimals
        double PI = 3.14;

        //a single character no numbers special characters on keyboard etc
        char ch1 = '%';
        System.out.println(ch1);
        char genderFemale = 'F';
        //ASCII Code for hash is 35
        char ch4 = 35;
        System.out.println(ch4);
        System.out.println(genderFemale);

    }


}
