package day18_NestedLoop;

public class NestedLoopIntro {

    public static void main(String[] args) {

        for (int i = 0; i < 2; i++) {
            System.out.println("Wooden Spoon");
            for (int j = 0; j < 3; j++) {
                System.out.println("Hello");
            }
        }
        /*
Wooden Spoon
Hello
Hello
Hello
Wooden Spoon
Hello
Hello
Hello
         */


    }


}
